package com.delains.ui.utils;

import de.jensd.fx.glyphs.fontawesome.FontAwesomeIcon;
import de.jensd.fx.glyphs.fontawesome.FontAwesomeIconView;

public class FontAwesomeUtils {

    public  static FontAwesomeIconView fontAwesomeRemove() {return new FontAwesomeIconView(FontAwesomeIcon.REMOVE);}

    public  static FontAwesomeIconView fontAwesomeSave(){return new FontAwesomeIconView(FontAwesomeIcon.SAVE);}

    public  static FontAwesomeIconView fontAwesomeUpdate(){return new FontAwesomeIconView(FontAwesomeIcon.EDIT);}

    public  static FontAwesomeIconView fontAwesomeAdd(){return new FontAwesomeIconView(FontAwesomeIcon.PLUS);}

    public  static FontAwesomeIconView fontAwesomePrint(){return new FontAwesomeIconView(FontAwesomeIcon.PRINT);}

}
